public class Produs {
    String nume, categorie;
    Double pret;
    Disponibilitate disponibilitate;

    public Produs(String nume, String categorie, Double pret, Disponibilitate disponibilitate) {
        this.nume = nume;
        this.categorie = categorie;
        this.pret = pret;
        this.disponibilitate = disponibilitate;
    }
}
